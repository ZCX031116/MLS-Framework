"""Multi-CE constraint sets used by run_multi.py."""


def _interval(src, dst, value, eps, scale=1.0):
    return [
        (src, dst, ">", value - eps, scale),
        (src, dst, "<", value + eps, scale),
    ]


def _intervals(specs, eps):
    constraints = []
    for src, dst, value in specs:
        constraints.extend(_interval(src, dst, value, eps))
    return constraints


